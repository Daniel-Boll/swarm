#include <spdlog/spdlog.h>

#include <swarm/network/manager.hpp>
#include <swarm/network/payload/player-position.hpp>

int main() {
  using namespace swarm;

  network::Manager server;

  if (!server.createServer(12345, 32)) {
    spdlog::error("Failed to create server");
    throw std::runtime_error("Failed to create server");
  }

  spdlog::info("Server created successfully");

  server.registerPacketHandler<network::PlayerPositionPacket>(
      [&server](const network::PlayerPositionPacket& packet, ENetPeer* sender) {
        server.broadcastToOthers<network::PlayerPositionPacket>({packet, sender->connectID},
                                                                sender);
      });

  while (true) {
    server.update();
    std::this_thread::sleep_for(std::chrono::milliseconds(16));  // ~60 fps
  }

  return 0;
}
